def display(board):
    for i in range(len(board)):
        if i % 3 == 0 and i != 0:
            print("---" * 7)
        for j in range(len(board[i])):
            if j % 3 == 0 and j != 0:
                print("|", end = " ")
            print(board[i][j], end = " ")
        print()

    return None

